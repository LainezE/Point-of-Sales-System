    private void submitBtnASActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnASActionPerformed
        int selectedRow = inventoryTable.getSelectedRow();
        if (selectedRow < 0)
            return;
        
        if (!UI_Tools.isInteger(asAmt.getText()))
            return;
        
        int amount = Integer.valueOf(asAmt.getText());
        String serial = inventoryTable.getModel().getValueAt(
                inventoryTable.convertRowIndexToModel(selectedRow), 2).toString();
        
        pos.inventory.updateStock(serial, amount);
        
        updateInventoryTableData();
        dlgAddStock.dispose();
    }//GEN-LAST:event_submitBtnASActionPerformed

