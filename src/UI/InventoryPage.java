    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        int selectedRow = inventoryTable.getSelectedRow();
        if (selectedRow < 0)
            return;
        selectedRow = inventoryTable.convertRowIndexToModel(selectedRow);
        
        DefaultTableModel model = (DefaultTableModel)inventoryTable.getModel();
        String modelName = (String)model.getValueAt(selectedRow, 0);
        int modelNumber = (int)model.getValueAt(selectedRow, 1);
        String serialNumber = model.getValueAt(selectedRow, 2).toString();
        int currStock = (int)model.getValueAt(selectedRow, 3);
        BigDecimal cost = BigDecimal.valueOf(Double.valueOf(model.getValueAt(selectedRow, 4).toString()));
        BigDecimal listPrice = BigDecimal.valueOf(Double.valueOf(model.getValueAt(selectedRow, 5).toString()));
        BigDecimal msrp = BigDecimal.valueOf(Double.valueOf(model.getValueAt(selectedRow, 6).toString()));
        String manufacturer = (String)model.getValueAt(selectedRow, 7);
        int baseStock = (int)model.getValueAt(selectedRow, 8);
        int reOrderAmt = (int)model.getValueAt(selectedRow, 9);
        
        modNameField1.setText(modelName);
        modNumField1.setText(String.valueOf(modelNumber));
        serialNumField1.setText(String.valueOf(serialNumber));
        currStockField1.setText(String.valueOf(currStock));
        costField1.setText(String.valueOf(cost));
        listPriceField1.setText(String.valueOf(listPrice));
        msrpField1.setText(String.valueOf(msrp));
        manufacturerField1.setText(manufacturer);
        baseStockField1.setText(String.valueOf(baseStock));
        reOrderAmtField1.setText(String.valueOf(reOrderAmt));
        
        UI_Tools.fixWindow(dlgEditInventory);
        dlgEditInventory.setVisible(true);
        disableMainPanel();
    }//GEN-LAST:event_editBtnActionPerformed
   
