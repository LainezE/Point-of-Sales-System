    private void updateInventoryTableData() {
        DefaultTableModel model = (DefaultTableModel) inventoryTable.getModel();
        
        model.setRowCount(0);
        InventoryItem[] inventoryList = pos.inventory.loadAll();
        for (InventoryItem item : inventoryList)
            model.addRow(new Object[] {
                item.getModelName(), item.getModelNumber()
                , item.getSerialNumber(), item.getStock(), item.getCost()
                , item.getListPrice(), item.getMSRP(), item.getManufacturer()
                , item.getBaseStock(), item.getReorderAmount()
            });
        
     
    }

