    private void btnEditEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditEmployeeActionPerformed
        int selectedRow = employeeTable.getSelectedRow();
        if (selectedRow < 0)
            return;
        
        int employeeid = (int)employeeTable.getModel().getValueAt(
                employeeTable.convertRowIndexToModel(selectedRow), 0);
        Employee employee = pos.employees.load(employeeid);
        
        eeFirstName.setText(employee.getFirstName());
        eeLastName.setText(employee.getLastName());
        eeTitle.setText(employee.getTitle());
        eePhone.setText(employee.getPhone());
        eeEmail.setText(employee.getEmail());
        eeStreet.setText(employee.getStreet());
        eeCity.setText(employee.getCity());
        eeState.setText(employee.getState());
        eeZip.setText("" + employee.getZipcode());
        
        UI_Tools.fixWindow(dlgEditEmployee);
        dlgEditEmployee.setVisible(true);
        disableMainPanel();
    }//GEN-LAST:event_btnEditEmployeeActionPerformed

