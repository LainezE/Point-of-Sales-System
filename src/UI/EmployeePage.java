    private void btncuSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncuSubmitActionPerformed
        int employeeid = Integer.valueOf(cuEmployeeID.getText());
        String username = cuUsername.getText();
        char[] password = cuPassword.getPassword();
        short accesslevel = 0;
        if (cuAccessLevel.getSelectedItem() == "Sales")
            accesslevel = PoS.SALES_LEVEL;
        else if (cuAccessLevel.getSelectedItem() == "Inventory")
            accesslevel = PoS.INVENTORY_LEVEL;
        else if (cuAccessLevel.getSelectedItem() == "Admin")
            accesslevel = PoS.ADMIN_LEVEL;
        
        pos.users.create(employeeid, username, password, accesslevel);
        dlgCreateUser.dispose();
        updateEmployeeTableData();
    }//GEN-LAST:event_btncuSubmitActionPerformed

