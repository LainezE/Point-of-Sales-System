    private void updateEmployeeTableData() {
        DefaultTableModel model = (DefaultTableModel)employeeTable.getModel();
        model.setRowCount(0);
        Employee[] employeeList = pos.employees.loadAll();
        for (Employee employee : employeeList)
            model.addRow(new Object[]{employee.getID(), employee.getFirstName()
                    , employee.getLastName(), employee.getTitle()
                    , employee.getPhone(), employee.getEmail()}
            );
    }
    
    
