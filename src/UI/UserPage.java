    private void updateUserTableData() {
        DefaultTableModel model = (DefaultTableModel)userTable.getModel();
        model.setRowCount(0);
        User[] userList = pos.users.loadAll();
        for (User user : userList)
            model.addRow(new Object[]{user.getID(), user.getEmployeeID()
                    , user.getUsername(), user.getAccessLevel()}
            );
    }
    
