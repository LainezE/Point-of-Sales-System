    private void btnEditUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditUserActionPerformed
        int selectedRow = userTable.getSelectedRow();
        if (selectedRow < 0)
            return;
        
        int userid = (int) userTable.getModel().getValueAt(
                userTable.convertRowIndexToModel(selectedRow), 0);
        int employeeid = (int) userTable.getModel().getValueAt(
                userTable.convertRowIndexToModel(selectedRow), 1);
        String username = userTable.getModel().getValueAt(
                userTable.convertRowIndexToModel(selectedRow), 2).toString();
        short accesslevel = (short)userTable.getModel().getValueAt(
                userTable.convertRowIndexToModel(selectedRow), 3);
        
        euEmployeeID.setText(String.valueOf(employeeid));
        euUsername.setText(username);
        switch (accesslevel) {
            case PoS.ADMIN_LEVEL:
                euAccessLevel.setSelectedIndex(2);
                break;
            case PoS.INVENTORY_LEVEL:
                euAccessLevel.setSelectedIndex(1);
                break;
            default:
                euAccessLevel.setSelectedIndex(0);
                break;
        }
        
        UI_Tools.fixWindow(dlgEditUser);
        dlgEditUser.setVisible(true);
        disableMainPanel();
    }//GEN-LAST:event_btnEditUserActionPerformed

