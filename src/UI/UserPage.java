    private void btneuSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneuSubmitActionPerformed
        int selectedRow = userTable.getSelectedRow();
        if (selectedRow < 0)
            return;
        
        if (!UI_Tools.isInteger(euEmployeeID.getText())) {
            System.out.println("Employee ID must be an integer.");
            return;
        }
        
        int userid = (int) userTable.getModel().getValueAt(
                userTable.convertRowIndexToModel(selectedRow), 0);
        
        int employeeid = Integer.valueOf(euEmployeeID.getText());
        String username = euUsername.getText();
        char[] password = euPassword.getPassword();
        short accesslevel = 0;
        switch (euAccessLevel.getSelectedIndex()) {
            case 0:
                accesslevel = PoS.SALES_LEVEL;
                break;
            case 1:
                accesslevel = PoS.INVENTORY_LEVEL;
                break;
            case 2:
                accesslevel = PoS.ADMIN_LEVEL;
                break;
        }
        
        if (!username.equals(""))
            pos.users.updateUsername(userid, username);
        if (accesslevel > 0)
            pos.users.updateAccessLevel(userid, accesslevel);
        if (!password.equals(""))
            pos.users.updatePassword(userid, password);
        
        updateUserTableData();
        dlgEditUser.dispose();
    }//GEN-LAST:event_btneuSubmitActionPerformed

